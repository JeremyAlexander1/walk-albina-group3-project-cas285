            <h2><a href="http://vafbcpdx.org/">VANCOUVER AVENUE FIRST BAPTIST CHURCH</a></h2>
                <img class="contentimage" src="assets/images/church1.png" alt="Vancouver Avenue First Baptist Church">
                <p>Since 1944, the Vancouver Avenue First Baptist Church has been the spiritual hub of Portland’s African American community. In 1961, the Reverend Martin Luther King, jr gave a speech there, extolling the necessity of integration.</p>
