                <h2>ABOUT</h2>
                <p>
                Walk Albina's Black History is a service learning project provided by Dr. Andrea Lowgren's HST 251 students. A guided walk will be held, covering topics such as: <br><br></p>
                
                <ul>
                <li>The Great Migration</li>
                <li>Civil Rights Movement</li>
                <li>Religious Organizing</li>
                <li>Realtor and neighborhood discrimination</li>
                <li>Segregated Schools</li>
                <li>Federal Urban Renewal Programs</li>
                <li>Jazz Music</li>
                <li>Urban Poverty</li>
                <li>Gentrification</li>
                </ul>
                
                <p>
                <br>
                The walk begins at 6pm on Tuesday, June 12, 2018. The trip consists of walking and standing for about 1 hour, and the total distance is 1.2 miles. Please note there is not a designated parking lot for Dawson Park. You can use side street parking, or bus lines 4 and 44. <br><br>
                
                Bring decent walking shoes, a water bottle, and possibly a light jacket depending on the weather. Bathrooms are available at Dawson Park. Note that the walk starts and ends at Dawson Park.
                </p>
            
