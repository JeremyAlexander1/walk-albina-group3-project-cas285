            <h2><a href="https://www.theskanner.com/news/northwest/26187-pps-says-it-will-reopen-tubman">HARRIET TUBMAN MIDDLE SCHOOL</a></h2>
                <img class="contentimage" src="assets/images/harriet-tubman-middle-school-2.jpg" alt="Harriet Tubman Middle School">
                <p>The story of Harriet Tubman Middle School is one of what is possible when people come together to voice their needs, and a part of the African American community’s continuing fight for quality education in their neighborhoods.</p>
        
