        <section id="fred-hampton">
            <h2><a href="https://oregonhistoryproject.org/articles/historical-records/albina-residents-picket-emanuel-hospital/#.WwSM3YgvyUk">FRED HAMPTON PEOPLE'S FREE HEALTH CLINIC</a></h2>
                <img class="contentimage" src="assets/images/fred-hampton.jpg" alt="Fred Hampton Free Health Clinic" >
                <p>Beginning in 1969, the Fred Hampton People’s Free Health Clinic provided free health care to anyone who walked in the door. In 1972, the building was razed to make way for an expansion of Legacy Emanuel Hospital. Over 40 years later, the lot still stands empty.</p>
        </section>
        
